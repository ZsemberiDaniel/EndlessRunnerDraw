    private static List<Color> allColors;

    static Colors() {
        allColors = new List<Color>();

        // get all colors from the classes
        var _innerClasses = (typeof(Colors)).GetNestedTypes();

        foreach (Type type in _innerClasses) {
            var _colorsInClass = type.GetFields(BindingFlags.Static | BindingFlags.Public)
                .Select(field => (Color) field.GetValue(null));

            allColors.AddRange(_colorsInClass);
        }
    }

    public static Color GetRandomColor() {
        return allColors[UnityEngine.Random.Range(0, allColors.Count)];
    }

